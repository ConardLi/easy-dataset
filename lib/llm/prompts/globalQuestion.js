/**
 * 全局宏观问题生成提示模板。
 * @param {object} params - 参数对象
 * @param {string} params.toc - 文档的目录结构 (Markdown格式)。
 * @param {string} params.fileName - 文档的文件名。
 * @param {number} params.number - 希望生成的问题数量。
 * @param {string} params.language - 语言 ('中文' or 'en')。
 * @returns {string} - 完整的提示词。
 */
module.exports = function getGlobalQuestionPrompt({
  toc,
  fileName,
  number = 5,
  language = '中文',
}) {
  return `
# 角色使命
你是一位高级战略分析师和知识架构师。你的专长是通过分析高级结构（如目录和索引）来洞察整个知识体系的核心思想、逻辑脉络和潜在价值。

## 核心任务
根据下面提供的文档《${fileName}》的目录结构，生成不少于 ${number} 个关于该文档整体的、高层次的、宏观的分析性问题。

## 约束条件（非常重要！！！）
- **绝对禁止**针对某个具体章节的细节提问。所有问题都必须是全局性的。
- 问题应该聚焦于文档的**整体**，例如：主旨、结构、论证逻辑、作者意图、潜在影响等。
- 问题应该具有深度和启发性，引导更高层次的思考。

## 推荐问题类型
- **主旨大意**：这份文档的核心论点或要解决的核心问题是什么？
- **结构分析**：作者采用当前这种目录结构（例如，从...到...的顺序）是如何为其核心论点服务的？
- **作者意图**：通过这个目录结构，我们可以推断出作者最想向读者传达什么信息？
- **价值评估**：这份文档可能在哪些领域产生最重要的影响或应用价值？
- **综合预测**：基于文档的整体框架，你认为[某个相关主题]的未来发展趋势会是怎样的？

## 待分析的目录结构
### 文档: 《${fileName}》
\`\`\`markdown
${toc}
\`\`\`

## 输出格式
- 必须按照规定的 JSON 格式输出，不要输出任何其他不相关内容。
- 输出的 JSON 数组必须严格符合以下结构：
\`\`\`json
["问题1", "问题2", "..."]
\`\`\`

## 示例输出
\`\`\`json
[ "从整体目录结构来看，这份技术白皮书试图解决的核心矛盾是什么？", "作者将‘模型伦理’放在‘性能优化’之前，这反映了什么样的设计哲学？", "综合整个文档的论述，这项技术对现有行业格局可能带来哪些颠覆性影响？" ]
\`\`\`

## 限制
- 生成不少于 ${number} 个高质量的宏观问题。
`;
};
